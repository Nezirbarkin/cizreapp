-- App About Settings Table
-- Bu tablo "Hakkında" ekranındaki bilgileri tutar ve admin tarafından düzenlenebilir

-- Tablo oluştur
CREATE TABLE IF NOT EXISTS public.app_about_settings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  app_name TEXT NOT NULL DEFAULT 'Cizre App',
  app_slogan TEXT NOT NULL DEFAULT 'Cizre''nin En Büyük Alışveriş Platformu',
  app_description TEXT NOT NULL,
  app_features TEXT[] NOT NULL DEFAULT ARRAY[
    'Mağaza ve ürün yönetimi',
    'Sipariş takibi',
    'Sosyal paylaşım ve etkileşim',
    'Anlık mesajlaşma',
    'Favori ürünler ve mağazalar',
    'Ürün değerlendirme sistemi'
  ],
  contact_email TEXT NOT NULL DEFAULT 'destek@cizreapp.com',
  website_url TEXT NOT NULL DEFAULT 'https://cizreapp.com',
  support_phone TEXT,
  terms_of_service TEXT NOT NULL,
  privacy_policy TEXT NOT NULL,
  version_number TEXT NOT NULL DEFAULT '1.0.0',
  build_number TEXT NOT NULL DEFAULT '1',
  social_media_links JSONB DEFAULT '{
    "instagram": "",
    "twitter": "",
    "facebook": "",
    "youtube": ""
  }'::jsonb,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

