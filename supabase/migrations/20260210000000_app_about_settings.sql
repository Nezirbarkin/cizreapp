-- updated_at trigger
CREATE OR REPLACE FUNCTION update_app_about_settings_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER app_about_settings_updated_at
  BEFORE UPDATE ON public.app_about_settings
  FOR EACH ROW
  EXECUTE FUNCTION update_app_about_settings_updated_at();

-- RLS Politikaları
ALTER TABLE public.app_about_settings ENABLE ROW LEVEL SECURITY;

-- Herkes okuyabilir
CREATE POLICY "app_about_settings_select_policy"
ON public.app_about_settings
FOR SELECT
TO authenticated
USING (true);

-- Sadece admin güncelleyebilir
CREATE POLICY "app_about_settings_update_policy"
ON public.app_about_settings
FOR UPDATE
TO authenticated
USING (EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin'))
WITH CHECK (EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin'));
